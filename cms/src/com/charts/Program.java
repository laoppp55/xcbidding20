package com.charts;

public class Program {

  private int id;
  private int p_type;
  private int position;
  private int l_type;
  private String explain;
  private String notes;
  private String positionstr;
  private String p_typestr;
  private String l_typestr;
  private String program;

  public Program() {
  }

  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

  public int getP_type() {
    return p_type;
  }

  public void setP_type(int p_type) {
    this.p_type = p_type;
  }

  public int getPosition() {
    return position;
  }

  public void setPosition(int position) {
    this.position = position;
  }

  public int getL_type() {
    return l_type;
  }

  public void setL_type(int l_type) {
    this.l_type = l_type;
  }

  public String getExplain() {
    return explain;
  }

  public void setExplain(String explain) {
    this.explain = explain;
  }

  public String getNotes() {
    return notes;
  }

  public void setNotes(String notes) {
    this.notes = notes;
  }

  public String getPositionstr() {
    return positionstr;
  }

  public void setPositionstr(String positionstr) {
    this.positionstr = positionstr;
  }

  public String getP_typestr() {
    return p_typestr;
  }

  public void setP_typestr(String p_typestr) {
    this.p_typestr = p_typestr;
  }

  public String getProgram() {
    return program;
  }

  public void setProgram(String program) {
    this.program = program;
  }

  public String getL_typestr() {
    return l_typestr;
  }

  public void setL_typestr(String l_typestr) {
    this.l_typestr = l_typestr;
  }
}