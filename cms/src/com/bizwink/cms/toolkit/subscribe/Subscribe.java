public class Subscribe {
    private int id;
    private String email;
    private int subflag;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public int getSubflag() {
        return subflag;
    }

    public void setSubflag(int subflag) {
        this.subflag = subflag;
    }
}
