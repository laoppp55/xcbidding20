package com.bizwink.cms.bjhqfw.jiben;


public class JiBen {

    private int id;
    private String meetname;
    private String meetmax;
    private String meetroot;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getMeetname() {
        return meetname;
    }

    public void setMeetname(String meetname) {
        this.meetname = meetname;
    }

    public String getMeetmax() {
        return meetmax;
    }

    public void setMeetmax(String meetmax) {
        this.meetmax = meetmax;
    }

    public String getMeetroot() {
        return meetroot;
    }

    public void setMeetroot(String meetroot) {
        this.meetroot = meetroot;
    }
}
